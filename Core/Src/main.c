/**
  * @brief  Function implementing the readSensor thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_initSensorRead */
void initSensorRead(void const * argument)
{
  /* USER CODE BEGIN 5 */
  /* Infinite loop */
  for(;;)
  {
	HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, 0);
	HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_14);
	i2cDataBuffer[0] = LM75_REG_TEMP;
	if(HAL_I2C_Master_Transmit(&hi2c3, ((uint8_t)LM75_ADDR)<<1, i2cDataBuffer, 1, HAL_MAX_DELAY) == HAL_OK){
		HAL_I2C_Master_Receive(&hi2c3, ((uint8_t)LM75_ADDR)<<1, i2cDataBuffer, 2, HAL_MAX_DELAY);
		int16_t *ptr = &temp;
		*ptr = ((i2cDataBuffer[0] << 8) | i2cDataBuffer[1]);
	}
	else
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, 1);
	osDelay(5000);
  }
  /* USER CODE END 5 */
}

/* USER CODE BEGIN Header_initDisplayWrite */
/**
* @brief Function implementing the writeDisplay thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_initDisplayWrite */
void initDisplayWrite(void const * argument)
{
  /* USER CODE BEGIN initDisplayWrite */
  float tempCelsius;
  float tempFar;

  HD44780_Init(2);
  /* Infinite loop */
  for(;;)
  {
	  HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_13);
	  display[0] = '\0';
	  tempCelsius = temp / 256.0;
	  tempFar = (temp/256.0)*(9.0/5.0)+32.0;

	  HD44780_Clear();
	  HD44780_SetCursor(0,0);
	  sprintf(display, "Far: %.3f", tempFar);
	  HD44780_PrintStr(display);
	  HD44780_SetCursor(0,1);
	  sprintf(display, "Celsius: %.3f", tempCelsius);
	  HD44780_PrintStr(display);
	  osDelay(5000);
  }
  /* USER CODE END initDisplayWrite */
}

